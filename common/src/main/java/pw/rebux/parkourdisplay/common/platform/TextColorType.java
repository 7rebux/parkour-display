package pw.rebux.parkourdisplay.common.platform;

/// Platform-neutral named colors used by domain chat messages. Each platform maps these onto its
/// own color type (LabyMod `NamedTextColor`, Fabric `Formatting`).
public enum TextColorType {
  GRAY,
  RED,
  GREEN,
  DARK_GREEN,
  DARK_RED,
  AQUA,
  DARK_AQUA
}
